#include<bits/stdc++.h>
using namespace std;

int main(){
    int x=3, y=6;
    cout<<"bitwise AND: \n";
    cout<<(x&y);
    cout<<"\nbitwise OR: \n";
    cout<<(x|y);
    cout<<"\nbitwise XOR: \n";
    cout<<(x^y);
    cout<<"\nLeftshift operator: \n";
    cout<<(x<<2);
    cout<<"\nRightshift operator: \n";
    cout<<(33>>2);
}